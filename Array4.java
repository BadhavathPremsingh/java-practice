import java.io.*;
import java.util.*;
class Array5
{
  public static void main(String[] args)
  {
    int row=3,col=3;
    int arr[][]=new int[row][col];
    for(int i=0;i<row;i++)
    {
     for(int j=0;i<arr[row].length;j++)
     {
       System.out.print(arr[i][j]);
      }
     }
   }
}
    